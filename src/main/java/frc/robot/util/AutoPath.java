package frc.robot.util;

// Codenames for different auto path follow a snake naming convention
// because auto paths look like snakes

// Python = Move forwards
// Boa = Move forwards and score
// Cobra = Move forwards, score, and pick up 3 from trench
// Viper = Move forwards, score, pick up 3 from trench, and score again
public enum AutoPath {
    PYTHON, BOA, COBRA, VIPER;
}